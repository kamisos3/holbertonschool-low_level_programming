#include "3-calc.h"
/**
* op_add - Sums two integers
* @a: first integer
* @b: second integer
* Return: Sum of two integers
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
* op_sub - Substract with two integers
* @a: first integer
* @b: second integer
* Return: Substraction remainder
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
* op_mul - Multiplies two integers
* @a: first interger
* @b: second integer
* Return: Multiplication result
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
* op_div - Divides between two integers
* @a: first integer
* @b: second integer
* Return: Division result
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
* op_mod - Remainder of a division
* @a: first integer
* @b: second integer
* Return: Remainder
*/
int op_mod(int a, int b)
{
	return (a % b);
}
